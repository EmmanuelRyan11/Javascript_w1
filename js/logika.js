// Operator Pembanding
let x = 8;
let y = 7;

// lebih besar
console.log(x > y);

// lebih kecil
console.log(x < y);

// sama dengan
console.log(x == y);

// lebih besar sama dengan
console.log(x >= y);

// lebih kecil sama dengan
console.log(x <= y);

